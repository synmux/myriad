class AntiLGBTQTimeline {
  constructor() {
    this.events = timelineData.timeline_events.sort(
      (a, b) => new Date(a.date) - new Date(b.date),
    );
    this.categories = timelineData.categories;
    this.filteredEvents = [...this.events];

    // DOM elements
    this.searchInput = document.getElementById("searchInput");
    this.categoryFilter = document.getElementById("categoryFilter");
    this.startDateInput = document.getElementById("startDate");
    this.endDateInput = document.getElementById("endDate");
    this.timelineContainer = document.getElementById("timelineEvents");
    this.noResultsElement = document.getElementById("noResults");
    this.totalEventsElement = document.getElementById("totalEvents");
    this.categoryBreakdownElement =
      document.getElementById("categoryBreakdown");

    this.init();
  }

  init() {
    this.bindEvents();
    this.renderTimeline();
    this.updateStatistics();
    this.observeTimelineEvents();
  }

  bindEvents() {
    // Search functionality with debounce
    this.searchInput.addEventListener("input", (e) => {
      this.debounce(() => this.applyFilters(), 300)();
    });

    // Category filter
    this.categoryFilter.addEventListener("change", () => {
      this.applyFilters();
    });

    // Date range filters
    this.startDateInput.addEventListener("change", () => this.applyFilters());
    this.endDateInput.addEventListener("change", () => this.applyFilters());

    // Keyboard navigation
    document.addEventListener("keydown", (e) => {
      if (e.key === "Escape") {
        this.collapseAllEvents();
      }
    });
  }

  debounce(func, wait) {
    let timeout;
    return function executedFunction(...args) {
      const later = () => {
        clearTimeout(timeout);
        func(...args);
      };
      clearTimeout(timeout);
      timeout = setTimeout(later, wait);
    };
  }

  applyFilters() {
    const searchTerm = this.searchInput.value.toLowerCase().trim();
    const selectedCategory = this.categoryFilter.value;
    const startDateValue = this.startDateInput.value;
    const endDateValue = this.endDateInput.value;

    this.filteredEvents = this.events.filter((event) => {
      // Search filter - check title, description, and details
      const matchesSearch =
        !searchTerm ||
        event.title.toLowerCase().includes(searchTerm) ||
        event.description.toLowerCase().includes(searchTerm) ||
        event.details.toLowerCase().includes(searchTerm);

      // Category filter
      const matchesCategory =
        selectedCategory === "all" || event.category === selectedCategory;

      // Date filter - improved logic to handle date strings properly
      let matchesDate = true;
      if (startDateValue && endDateValue) {
        const eventDate = event.date; // Keep as string for comparison
        matchesDate = eventDate >= startDateValue && eventDate <= endDateValue;
      }

      return matchesSearch && matchesCategory && matchesDate;
    });

    this.renderTimeline();
    this.updateStatistics();
  }

  formatDate(dateString) {
    const options = {
      year: "numeric",
      month: "long",
      day: "numeric",
    };
    return new Date(dateString).toLocaleDateString("en-US", options);
  }

  getCategoryLabel(categoryName) {
    const category = this.categories.find((cat) => cat.name === categoryName);
    return category ? category.label : categoryName;
  }

  createTimelineEventHTML(event, index) {
    return `
      <div class="timeline-event category-${event.category}" data-id="${event.id}" data-index="${index}">
        <div class="timeline-event-card category-${event.category}">
          <div class="timeline-event-header">
            <div class="timeline-event-date">${this.formatDate(event.date)}</div>
            <div class="timeline-event-category category-${event.category}">
              ${this.getCategoryLabel(event.category)}
            </div>
            <h3 class="timeline-event-title">${event.title}</h3>
          </div>
          
          <p class="timeline-event-description">${event.description}</p>
          
          <div class="timeline-event-details" id="details-${event.id}">
            <p>${event.details}</p>
          </div>
          
          <div class="expand-indicator" data-event-id="${event.id}">
            ▼ Show Full Details
          </div>
        </div>
      </div>
    `;
  }

  renderTimeline() {
    if (this.filteredEvents.length === 0) {
      this.timelineContainer.innerHTML = "";
      this.noResultsElement.classList.remove("hidden");
      return;
    }

    this.noResultsElement.classList.add("hidden");

    const timelineHTML = this.filteredEvents
      .map((event, index) => this.createTimelineEventHTML(event, index))
      .join("");

    this.timelineContainer.innerHTML = timelineHTML;

    // Add click handlers for expand/collapse
    this.addExpandHandlers();

    // Add accessibility attributes
    this.addAccessibilityAttributes();

    // Trigger animation for new elements
    setTimeout(() => {
      this.animateTimelineEvents();
    }, 100);
  }

  addExpandHandlers() {
    const expandIndicators = document.querySelectorAll(".expand-indicator");

    expandIndicators.forEach((indicator) => {
      indicator.addEventListener("click", (e) => {
        e.stopPropagation();
        const eventId = indicator.dataset.eventId;
        this.toggleEventDetails(eventId, indicator);
      });
    });

    // Also allow clicking on the card to expand
    const eventCards = document.querySelectorAll(".timeline-event-card");
    eventCards.forEach((card) => {
      card.addEventListener("click", (e) => {
        // Don't trigger if clicking on the expand indicator
        if (e.target.classList.contains("expand-indicator")) return;

        const eventElement = card.closest(".timeline-event");
        const eventId = eventElement.dataset.id;
        const indicator = card.querySelector(".expand-indicator");
        this.toggleEventDetails(eventId, indicator);
      });
    });
  }

  addAccessibilityAttributes() {
    const eventCards = document.querySelectorAll(".timeline-event-card");
    eventCards.forEach((card) => {
      card.setAttribute("tabindex", "0");
      card.setAttribute("role", "button");
      card.setAttribute("aria-label", "Click to expand event details");

      card.addEventListener("keydown", (e) => {
        if (e.key === "Enter" || e.key === " ") {
          e.preventDefault();
          card.click();
        }
      });
    });
  }

  toggleEventDetails(eventId, indicator) {
    const detailsElement = document.getElementById(`details-${eventId}`);
    const isExpanded = detailsElement.classList.contains("expanded");

    if (isExpanded) {
      detailsElement.classList.remove("expanded");
      indicator.classList.remove("rotated");
      indicator.textContent = "▼ Show Full Details";
    } else {
      detailsElement.classList.add("expanded");
      indicator.classList.add("rotated");
      indicator.textContent = "▲ Hide Details";
    }
  }

  collapseAllEvents() {
    const expandedDetails = document.querySelectorAll(
      ".timeline-event-details.expanded",
    );
    const indicators = document.querySelectorAll(".expand-indicator.rotated");

    expandedDetails.forEach((detail) => detail.classList.remove("expanded"));
    indicators.forEach((indicator) => {
      indicator.classList.remove("rotated");
      indicator.textContent = "▼ Show Full Details";
    });
  }

  updateStatistics() {
    // Update total events count
    this.totalEventsElement.textContent = this.filteredEvents.length;

    // Calculate category breakdown
    const categoryStats = {};
    this.categories.forEach((category) => {
      categoryStats[category.name] = {
        count: 0,
        label: category.label,
        color: category.color,
      };
    });

    this.filteredEvents.forEach((event) => {
      if (categoryStats[event.category]) {
        categoryStats[event.category].count++;
      }
    });

    // Render category breakdown
    const breakdownHTML = Object.entries(categoryStats)
      .filter(([_, stats]) => stats.count > 0)
      .map(
        ([categoryName, stats]) => `
        <div class="category-stat ${categoryName}">
          <div class="category-stat-number">${stats.count}</div>
          <div class="category-stat-label">${stats.label}</div>
        </div>
      `,
      )
      .join("");

    this.categoryBreakdownElement.innerHTML = breakdownHTML;
  }

  animateTimelineEvents() {
    const timelineEvents = document.querySelectorAll(".timeline-event");

    timelineEvents.forEach((event, index) => {
      setTimeout(() => {
        event.classList.add("visible");
      }, index * 100);
    });
  }

  observeTimelineEvents() {
    // Use Intersection Observer for scroll animations
    const observer = new IntersectionObserver(
      (entries) => {
        entries.forEach((entry) => {
          if (entry.isIntersecting) {
            entry.target.classList.add("visible");
          }
        });
      },
      {
        threshold: 0.1,
        rootMargin: "50px",
      },
    );

    // Re-observe elements when they're re-rendered
    const checkForNewElements = () => {
      const timelineEvents = document.querySelectorAll(
        ".timeline-event:not(.visible)",
      );
      timelineEvents.forEach((event) => {
        observer.observe(event);
      });
    };

    // Initial observation
    setTimeout(checkForNewElements, 200);

    // Re-observe after filtering
    const originalRenderTimeline = this.renderTimeline.bind(this);
    this.renderTimeline = function () {
      originalRenderTimeline();
      setTimeout(checkForNewElements, 200);
    };
  }
}

// Smooth scrolling and enhanced interactions
function addEnhancedInteractions() {
  // Smooth scroll to top functionality
  let scrollToTopButton = document.createElement("button");
  scrollToTopButton.innerHTML = "↑";
  scrollToTopButton.className = "scroll-to-top";
  scrollToTopButton.style.cssText = `
    position: fixed;
    bottom: 20px;
    right: 20px;
    background: var(--catppuccin-blue);
    color: var(--catppuccin-base);
    border: none;
    border-radius: 50%;
    width: 50px;
    height: 50px;
    font-size: 20px;
    cursor: pointer;
    opacity: 0;
    transition: all 0.3s ease;
    z-index: 1000;
  `;

  document.body.appendChild(scrollToTopButton);

  scrollToTopButton.addEventListener("click", () => {
    window.scrollTo({ top: 0, behavior: "smooth" });
  });

  // Show/hide scroll to top button
  window.addEventListener("scroll", () => {
    if (window.pageYOffset > 300) {
      scrollToTopButton.style.opacity = "1";
    } else {
      scrollToTopButton.style.opacity = "0";
    }
  });

  // Add keyboard shortcuts
  document.addEventListener("keydown", (e) => {
    // Ctrl/Cmd + K to focus search
    if ((e.ctrlKey || e.metaKey) && e.key === "k") {
      e.preventDefault();
      document.getElementById("searchInput").focus();
    }
  });

  // Add focus management for better accessibility
  const focusableElements =
    "input, select, .timeline-event-card, .expand-indicator";
  const focusable = Array.from(document.querySelectorAll(focusableElements));

  // Improve keyboard navigation
  document.addEventListener("keydown", (e) => {
    if (e.key === "Tab") {
      const currentFocus = document.activeElement;
      const currentIndex = focusable.indexOf(currentFocus);

      if (e.shiftKey) {
        // Shift + Tab (previous)
        if (currentIndex > 0) {
          focusable[currentIndex - 1].focus();
        }
      } else {
        // Tab (next)
        if (currentIndex < focusable.length - 1) {
          focusable[currentIndex + 1].focus();
        }
      }
    }
  });
}

// Initialize the timeline when DOM is loaded
document.addEventListener("DOMContentLoaded", () => {
  new AntiLGBTQTimeline();
  addEnhancedInteractions();

  // Add loading animation
  document.body.style.opacity = "0";
  document.body.style.transform = "translateY(20px)";
  document.body.style.transition = "all 0.5s ease";

  setTimeout(() => {
    document.body.style.opacity = "1";
    document.body.style.transform = "translateY(0)";
  }, 100);
});
